#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const artifactPath = path.join(repoRoot, 'packages/contracts/abi/IClanWorld.json');
const targetPath = path.join(repoRoot, 'packages/shared/src/adapters/IChainClient.ts');

const startMarker = '// BEGIN GENERATED CLAN_WORLD_ABI';
const endMarker = '// END GENERATED CLAN_WORLD_ABI';

const functionNames = [
  'getWorldState',
  'getClan',
  'getClansman',
  'getActiveMission',
  'getBanditTroop',
  'getScheduledMarketActionsForTick',
  'getDerivedClanState',
  'getDerivedClansmanState',
  'getWorldSnapshot',
  'getClanFullView',
  'getMarketState',
  'getActiveBanditView',
  'getWallUpgradeCost',
  'getBaseUpgradeCost',
  'getMonumentUpgradeCost',
  'getClanScore',
  'getRankings',
  'submitClanOrders',
  'transferGold',
  'transferVaultResource',
  'transferBlueprint',
  'transferBundle',
  'transferClanOwnership',
];

const eventNames = [
  'GoldTransferred',
  'VaultResourceTransferred',
  'BlueprintTransferred',
  'ClanOwnershipTransferred',
];

function readCanonicalAbi() {
  const parsed = JSON.parse(fs.readFileSync(artifactPath, 'utf8'));
  const abi = Array.isArray(parsed) ? parsed : parsed.abi;
  if (!Array.isArray(abi)) {
    throw new Error(`${artifactPath} must contain an ABI array or an "abi" array`);
  }
  return abi;
}

function generatedBlock() {
  const abi = readCanonicalAbi();
  const functionFragments = functionNames.map((name) => {
    const fragment = abi.find((entry) => entry.type === 'function' && entry.name === name);
    if (!fragment) {
      throw new Error(`Missing ${name} in ${artifactPath}`);
    }
    return fragment;
  });
  const eventFragments = eventNames.map((name) => {
    const fragment = abi.find((entry) => entry.type === 'event' && entry.name === name);
    if (!fragment) {
      throw new Error(`Missing ${name} in ${artifactPath}`);
    }
    return fragment;
  });

  const json = JSON.stringify([...functionFragments, ...eventFragments], null, 2);

  return [
    startMarker,
    '// Generated by scripts/gen-chainclient-abi.mjs from packages/contracts/abi/IClanWorld.json.',
    '// Run `pnpm gen:chainclient-abi` after updating the canonical contract ABI.',
    `export const CLAN_WORLD_ABI = ${json} as const;`,
    endMarker,
  ].join('\n');
}

function replaceGeneratedBlock(source, replacement) {
  const start = source.indexOf(startMarker);
  const end = source.lastIndexOf(endMarker);
  if (start === -1 || end === -1 || end < start) {
    const legacyStart = source.indexOf('// Minimal ABI');
    const legacyEnd = source.indexOf('class StubChainClient');
    if (legacyStart === -1 || legacyEnd === -1 || legacyEnd < legacyStart) {
      throw new Error(`Could not find generated ABI markers in ${targetPath}`);
    }
    return `${source.slice(0, legacyStart)}${replacement}\n\n${source.slice(legacyEnd)}`;
  }
  const conflictMarker = /^(<{7}|={7}|>{7})/m;
  const before = source.slice(0, start);
  const after = source.slice(end + endMarker.length);
  if (conflictMarker.test(before) || conflictMarker.test(after)) {
    throw new Error(`Resolve non-generated merge conflicts in ${targetPath} before regenerating.`);
  }

  return `${source.slice(0, start)}${replacement}${source.slice(end + endMarker.length)}`;
}

const check = process.argv.includes('--check');
const current = fs.readFileSync(targetPath, 'utf8');
const next = replaceGeneratedBlock(current, generatedBlock());

if (check) {
  if (next !== current) {
    console.error('IChainClient ABI fragment is out of date. Run `pnpm gen:chainclient-abi`.');
    process.exit(1);
  }
  console.log('IChainClient ABI fragment is up to date.');
} else {
  fs.writeFileSync(targetPath, next);
  console.log(`Updated ${path.relative(repoRoot, targetPath)} from ${path.relative(repoRoot, artifactPath)}.`);
}
