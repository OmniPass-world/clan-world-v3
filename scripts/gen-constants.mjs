#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '..');
const sourcePath = path.join(repoRoot, 'packages/contracts/src/IClanWorld.sol');
const targetPath = path.join(repoRoot, 'packages/shared/src/generated/constants.ts');

function stripComments(source) {
  return source
    .replace(/\/\*[\s\S]*?\*\//g, '')
    .replace(/\/\/.*$/gm, '');
}

function toBigIntLiteral(value) {
  const normalized = value.replaceAll('_', '').toLowerCase();
  const [coefficient, exponent] = normalized.split('e');
  const digits = exponent === undefined ? coefficient : `${coefficient}${'0'.repeat(Number(exponent))}`;
  return `${digits}n`;
}

function parseConstants(source) {
  const cleanSource = stripComments(source);
  const libraryMatch = cleanSource.match(/library\s+ClanWorldConstants\s*\{([\s\S]*?)\n\}/);
  if (!libraryMatch) {
    throw new Error(`Missing ClanWorldConstants library in ${path.relative(repoRoot, sourcePath)}`);
  }

  const constants = [];
  const declarationPattern =
    /\buint(?:256|64|32|16|8)\s+(?:(?:public|internal|private|external)\s+)?constant\s+([A-Z_][A-Z_0-9]*)\s*=\s*([0-9_]+(?:e[0-9_]+)?);/g;
  let match;

  while ((match = declarationPattern.exec(libraryMatch[1])) !== null) {
    const [, name, value] = match;
    constants.push([name, toBigIntLiteral(value)]);
  }

  return constants;
}

function render(constants) {
  if (constants.length === 0) {
    throw new Error(`No numeric constants found in ${path.relative(repoRoot, sourcePath)}`);
  }

  return [
    '// Generated by scripts/gen-constants.mjs - do NOT edit by hand',
    '',
    ...constants.map(([name, value]) => `export const ${name} = ${value};`),
    '',
  ].join('\n');
}

const source = fs.readFileSync(sourcePath, 'utf8');
const output = render(parseConstants(source));
const check = process.argv.includes('--check');

if (check) {
  const current = fs.existsSync(targetPath) ? fs.readFileSync(targetPath, 'utf8') : '';
  if (current !== output) {
    console.error('Generated constants are out of date. Run `pnpm gen:constants`.');
    process.exit(1);
  }
  console.log('Generated constants are up to date.');
} else {
  fs.mkdirSync(path.dirname(targetPath), { recursive: true });
  fs.writeFileSync(targetPath, output);
  console.log(`Updated ${path.relative(repoRoot, targetPath)} from ${path.relative(repoRoot, sourcePath)}.`);
}
